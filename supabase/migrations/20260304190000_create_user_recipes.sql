    'private_recipes', id
FROM public.private_recipes;

-- Tabla de mapeo temporal (old_id → new_id por tipo)
CREATE TEMP TABLE ur_map AS
SELECT id AS new_id, _src_table, _src_id FROM public.user_recipes;

-- Verificación previa
DO $$
DECLARE
    free_count    integer;
    private_count integer;
    ur_count      integer;
BEGIN
    SELECT COUNT(*) INTO free_count FROM public.free_recipes;
    SELECT COUNT(*) INTO private_count FROM public.private_recipes;
    SELECT COUNT(*) INTO ur_count FROM public.user_recipes;

    IF ur_count <> free_count + private_count THEN
        RAISE EXCEPTION 'Migración incompleta: free_recipes=%, private_recipes=%, user_recipes=%',
            free_count, private_count, ur_count;
    END IF;
    RAISE NOTICE 'Migración OK: free=%, private=%, total=%', free_count, private_count, ur_count;
END $$;

