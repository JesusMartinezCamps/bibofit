CREATE TABLE IF NOT EXISTS public.user_recipes (
    id                    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id               uuid NOT NULL,
    type                  text NOT NULL DEFAULT 'free' CHECK (type IN ('free', 'private')),
    name                  text,
    instructions          text,
    day_meal_id           bigint,
    diet_plan_id          bigint,
    prep_time_min         integer,
    difficulty            text,
    status                text CHECK (status IN (
                              'pending', 'approved_private', 'approved_general',
                              'kept_as_free_recipe', 'rejected')),
    -- Versioning / linaje (FK se añaden después de migrar datos)
    parent_user_recipe_id bigint,
    parent_recipe_id      bigint,
    source_user_recipe_id bigint,
    created_at            timestamp with time zone DEFAULT now(),
    -- Columnas temporales de tracking (se eliminan al final)
    _src_table            text,
    _src_id               bigint
);

ALTER TABLE public.user_recipes OWNER TO postgres;

