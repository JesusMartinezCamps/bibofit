-- Free recipes → type='free', conservar status
INSERT INTO public.user_recipes (
    user_id, type, name, instructions, day_meal_id, diet_plan_id,
    prep_time_min, difficulty, status, parent_recipe_id, created_at,
    _src_table, _src_id
)
SELECT
    user_id, 'free', name, instructions, day_meal_id, diet_plan_id,
    prep_time_min, difficulty, status, parent_recipe_id, created_at,
    'free_recipes', id
FROM public.free_recipes;

-- Private recipes → type='private', status = NULL
