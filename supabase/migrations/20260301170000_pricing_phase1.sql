-- public read for active plans and linked entities
create policy "Public read active commercial plans"
on public.commercial_plans
for select
using (is_active = true);

create policy "Public read commercial features"
on public.commercial_plan_features
for select
using (exists (
  select 1
  from public.commercial_plans p
  where p.id = commercial_plan_features.plan_id
    and p.is_active = true
));

create policy "Public read commercial plan targets"
on public.commercial_plan_role_targets
for select
using (exists (
  select 1
  from public.commercial_plans p
  where p.id = commercial_plan_role_targets.plan_id
    and p.is_active = true
));

