-- Import from Notion CSV: exercises + places + mappings

-- 1) Places model for training zone.
create table if not exists public.training_places (
  id bigint generated by default as identity primary key,
  name text not null unique
);

create table if not exists public.exercise_places (
  exercise_id bigint not null references public.exercises(id) on delete cascade,
  place_id bigint not null references public.training_places(id) on delete cascade,
  primary key (exercise_id, place_id)
);

alter table public.training_places enable row level security;
alter table public.exercise_places enable row level security;

drop policy if exists "Allow read to all training_places" on public.training_places;
create policy "Allow read to all training_places" on public.training_places for select using (true);
drop policy if exists "Allow admin full access on training_places" on public.training_places;
create policy "Allow admin full access on training_places" on public.training_places for all using (
  exists (select 1 from public.user_roles ur join public.roles r on r.id = ur.role_id where ur.user_id = auth.uid() and r.role = 'admin')
) with check (
  exists (select 1 from public.user_roles ur join public.roles r on r.id = ur.role_id where ur.user_id = auth.uid() and r.role = 'admin')
);

drop policy if exists "Allow read to all exercise_places" on public.exercise_places;
create policy "Allow read to all exercise_places" on public.exercise_places for select using (true);
drop policy if exists "Allow admin full access on exercise_places" on public.exercise_places;
create policy "Allow admin full access on exercise_places" on public.exercise_places for all using (
  exists (select 1 from public.user_roles ur join public.roles r on r.id = ur.role_id where ur.user_id = auth.uid() and r.role = 'admin')
) with check (
  exists (select 1 from public.user_roles ur join public.roles r on r.id = ur.role_id where ur.user_id = auth.uid() and r.role = 'admin')
);

grant all on table public.training_places to anon, authenticated, service_role;
grant all on table public.exercise_places to anon, authenticated, service_role;
grant all on sequence public.training_places_id_seq to anon, authenticated, service_role;

