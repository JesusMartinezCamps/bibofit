-- Explicit joints from Notion
insert into public.exercise_joints (exercise_id, joint_id)
select e.id, j.id
from tmp_notion_seed_joints sj
join public.exercises e on lower(e.name)=lower(sj.exercise_name)
join public.joints j on lower(j.name)=lower(sj.joint_name)
on conflict do nothing;

-- Enriched fallback: derive joints from mapped muscles
insert into public.exercise_joints (exercise_id, joint_id)
select distinct e.id, mj.joint_id
from tmp_notion_seed_exercises se
join public.exercises e on lower(e.name)=lower(se.name)
join public.exercise_muscles em on em.exercise_id=e.id
join public.muscle_joints mj on mj.muscle_id=em.muscle_id
on conflict do nothing;
