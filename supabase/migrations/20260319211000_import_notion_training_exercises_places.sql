-- 3) Insert missing exercises from Notion and backfill metadata when empty.
insert into public.exercises (name, unilateral, equipment_id, technique)
select s.name, s.unilateral, eq.id, s.technique
from tmp_notion_seed_exercises s
join public.equipment eq on lower(eq.name)=lower(s.equipment_name)
where not exists (select 1 from public.exercises e where lower(e.name)=lower(s.name));

update public.exercises e
set technique = coalesce(e.technique, s.technique),
    unilateral = coalesce(e.unilateral, s.unilateral),
    equipment_id = coalesce(e.equipment_id, eq.id)
from tmp_notion_seed_exercises s
join public.equipment eq on lower(eq.name)=lower(s.equipment_name)
where lower(e.name)=lower(s.name);

