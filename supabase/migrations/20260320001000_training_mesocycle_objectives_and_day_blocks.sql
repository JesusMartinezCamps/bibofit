-- Objectives catalog + multi-block day structure for mesocycle creation flow

-- 1) Objectives catalog
create table if not exists public.training_objectives (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null unique,
  description text,
  is_active boolean not null default true,
  display_order integer not null default 100,
  created_at timestamptz not null default now()
);

insert into public.training_objectives (code, name, description, display_order)
values
  ('hypertrophy', 'Ganancia de masa muscular', 'Enfoque en aumentar volumen de trabajo y progresar en repeticiones/carga.', 10),
  ('cardio', 'Mejora del cardio', 'Enfoque en capacidad cardiovascular, tolerancia al esfuerzo y recuperación.', 20),
  ('strength', 'Ganancia de fuerza', 'Enfoque en progresión de carga y mejora de rendimiento en patrones principales.', 30)
on conflict (code) do update
set name = excluded.name,
    description = excluded.description,
    display_order = excluded.display_order,
    is_active = true;

alter table public.training_objectives enable row level security;

drop policy if exists "Allow read to all training objectives" on public.training_objectives;
create policy "Allow read to all training objectives"
on public.training_objectives
for select
using (true);

drop policy if exists "Allow admin full access on training objectives" on public.training_objectives;
create policy "Allow admin full access on training objectives"
on public.training_objectives
for all
using (
  exists (
    select 1
    from public.user_roles ur
    join public.roles r on r.id = ur.role_id
    where ur.user_id = auth.uid()
      and r.role = 'admin'
  )
)
with check (
  exists (
    select 1
    from public.user_roles ur
    join public.roles r on r.id = ur.role_id
    where ur.user_id = auth.uid()
      and r.role = 'admin'
  )
);

grant all on table public.training_objectives to anon, authenticated, service_role;
grant all on sequence public.training_objectives_id_seq to anon, authenticated, service_role;

-- 2) Attach objective to mesocycle
alter table public.mesocycles
  add column if not exists objective_id bigint references public.training_objectives(id);

create index if not exists mesocycles_objective_id_idx
  on public.mesocycles (objective_id);

-- Backfill objective_id from legacy objective text where possible.
update public.mesocycles m
set objective_id = t.id
from public.training_objectives t
where m.objective_id is null
  and (
    (t.code = 'hypertrophy' and lower(coalesce(m.objective, '')) ~ '(masa|hipertrof|muscul)')
    or (t.code = 'cardio' and lower(coalesce(m.objective, '')) ~ '(cardio|resistenc|aerob)')
    or (t.code = 'strength' and lower(coalesce(m.objective, '')) ~ '(fuerza|strength)')
  );

update public.mesocycles m
set objective = t.name
from public.training_objectives t
where m.objective_id = t.id
  and coalesce(trim(m.objective), '') = '';

-- 3) Multiple focus blocks inside one training day/routine
create table if not exists public.routine_day_blocks (
  id bigint generated by default as identity primary key,
  routine_id bigint not null references public.routines(id) on delete cascade,
  block_order integer not null,
  block_type text not null,
  block_label text,
  created_at timestamptz not null default now(),
  unique (routine_id, block_order)
);

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conname = 'routine_day_blocks_block_order_check'
      and conrelid = 'public.routine_day_blocks'::regclass
  ) then
    alter table public.routine_day_blocks
      add constraint routine_day_blocks_block_order_check
      check (block_order >= 1);
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'routine_day_blocks_block_type_check'
      and conrelid = 'public.routine_day_blocks'::regclass
  ) then
    alter table public.routine_day_blocks
      add constraint routine_day_blocks_block_type_check
      check (
        block_type in (
          'torso','pierna','fullbody','push','pull','upper','lower','core','cardio','movilidad','custom'
        )
      );
  end if;
end
$$;

create index if not exists routine_day_blocks_routine_idx
  on public.routine_day_blocks (routine_id, block_order);

alter table public.routine_day_blocks enable row level security;

drop policy if exists "Allow admin full access on routine_day_blocks" on public.routine_day_blocks;
