// E.164: + cГіdigo de paГ­s + 6-14 dГ­gitos locales, mГЎximo 15 dГ­gitos totales (ITU-T E.164)
export const validatePhone = (prefix, number) => {
  if (!number) return null; // campo opcional
  if (!/^\d+$/.test(number)) return 'Solo se permiten dГ­gitos';
  if (number.length < 6) return 'Demasiado corto (mГ­nimo 6 dГ­gitos)';
  const totalDigits = prefix.replace('+', '').length + number.length;
  if (totalDigits > 15) return 'Demasiado largo (mГЎximo 15 dГ­gitos en total)';
  return null;
};

// Construye el nГєmero en formato E.164 listo para guardar en DB
// Devuelve null si el nГєmero local estГЎ vacГ­o (campo opcional)
export const buildE164 = (prefix, number) => {
  if (!number) return null;
  return `${prefix}${number}`;
};
